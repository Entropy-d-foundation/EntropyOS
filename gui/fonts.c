/* Storage for ASCII glyphs (32..127) */
static uint8_t ascii_font[96][FONT8X16_HEIGHT];
static uint8_t ascii_present[96] = {0};

/* Helper to parse a single line of 8 characters composed of '#' and '.' */
static uint8_t parse_line(const char *s)
{
        uint8_t v = 0;
        for (int i = 0; i < 8; ++i) {
                char ch = s && s[i] ? s[i] : '.';
                v <<= 1;
                if (ch == '#')
                        v |= 1;
        }
        return v;
}

int font8x16_register_from_text(uint8_t c, const char *lines[FONT8X16_HEIGHT])
{
        if (c < 32 || c > 127)
                return -1;

        uint8_t idx = c - 32;
        for (int i = 0; i < FONT8X16_HEIGHT; ++i) {
                ascii_font[idx][i] = parse_line(lines[i]);
        }
        ascii_present[idx] = 1;
        return 0;
}

const uint8_t *font8x16_get(uint8_t c)
{
        if (c < 32 || c > 127)
                return NULL;

        uint8_t idx = c - 32;
        if (!ascii_present[idx])
                return NULL;

        return ascii_font[idx];
}

