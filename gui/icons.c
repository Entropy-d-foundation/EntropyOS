void
draw_icon(EFI_GRAPHICS_OUTPUT_PROTOCOL *gop, const Icon *icon, uint32_t x, uint32_t y, UINT8 color)
{
        if (!icon || !gop)
                return;

        uint32_t pixel_color;

        /*
         * Convert EFI color to RGB
         */
        switch(color)
        {
                case EFI_BLACK:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 0, 0, 0);
                        break;
                case EFI_BLUE:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 0, 0, 255);
                        break;
                case EFI_GREEN:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 0, 255, 0);
                        break;
                case EFI_CYAN:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 0, 255, 255);
                        break;
                case EFI_RED:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 0, 0);
                        break;
                case EFI_MAGENTA:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 0, 255);
                        break;
                case EFI_BROWN:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 170, 85, 0);
                        break;
                case EFI_LIGHTGRAY:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 170, 170, 170);
                        break;
                case EFI_DARKGRAY:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 85, 85, 85);
                        break;
                case EFI_LIGHTBLUE:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 85, 85, 255);
                        break;
                case EFI_LIGHTGREEN:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 85, 255, 85);
                        break;
                case EFI_LIGHTCYAN:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 85, 255, 255);
                        break;
                case EFI_LIGHTRED:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 85, 85);
                        break;
                case EFI_LIGHTMAGENTA:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 85, 255);
                        break;
                case EFI_YELLOW:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 255, 85);
                        break;
                case EFI_WHITE:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 255, 255);
                        break;
                default:
                        pixel_color = pack_pixel(gop->Mode->Info->PixelFormat, 255, 255, 255);
                        break;
        }

        unsigned int *back = get_backbuffer_ptr();
        unsigned int back_w = get_backbuf_width();
        unsigned int back_h = get_backbuf_height();
        bool have_back = (back && back_w == gop->Mode->Info->HorizontalResolution && back_h == gop->Mode->Info->VerticalResolution);
        unsigned int pitch = gop->Mode->Info->PixelsPerScanLine;
        unsigned int *fb = (unsigned int*)(uintptr_t)gop->Mode->FrameBufferBase;

        for (int dy = 0; dy < ICON_SIZE; dy++)
        {
                if ((y + dy) < 0) continue;
                if ((y + dy) >= (int)gop->Mode->Info->VerticalResolution) break;

                if (have_back) {
                        unsigned int *row = back + (size_t)(y + dy) * back_w + x;
                        for (int dx = 0; dx < ICON_SIZE; dx++) {
                                if (icon->pixels[dy][dx] != '.') row[dx] = pixel_color;
                        }
                } else {
                        unsigned int *row = fb + (size_t)(y + dy) * pitch + x;
                        for (int dx = 0; dx < ICON_SIZE; dx++) {
                                if (icon->pixels[dy][dx] != '.') row[dx] = pixel_color;
                        }
                }
        }
}